//  Compound Operator  - possible interview questions

let x = 10;
x += 10; // x = x +10, += is just a shortcut that means "take what's already in the box, add this number to it.
console.log(x);


x -= 3;  // x = x -3
console.log(x);


x *= 2;  // x = x *2;
console.log(x);


x /= 17;  // x = x /17;
console.log(x);

x %= 2;
console.log(x);